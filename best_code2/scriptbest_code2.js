let currentLang = localStorage.getItem('language') || 'en';

// ===== Language Switcher =====
function setupLanguageSwitcher() {
    const langButtons = document.querySelectorAll('.lang-btn');
    langButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.dataset.lang;
            currentLang = lang;
            localStorage.setItem('language', lang);
            
            // Update active button
            langButtons.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            
            // Update HTML lang attribute
            document.documentElement.lang = lang;
            
            // Translate page
            translatePage();
        });
    });
    
    // Set initial active button
    langButtons.forEach(btn => {
        if (btn.dataset.lang === currentLang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
    
    // Set initial HTML lang
    document.documentElement.lang = currentLang;
}

// ===== Translate Page =====
function translatePage() {
    // Translate elements with data-translate
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(el => {
        const key = el.getAttribute('data-translate');
        if (translations[currentLang] && translations[currentLang][key]) {
            el.textContent = translations[currentLang][key];
        }
    });
    
    // Translate placeholders
    const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
    placeholderElements.forEach(el => {
        const key = el.getAttribute('data-translate-placeholder');
        if (translations[currentLang] && translations[currentLang][key]) {
            el.placeholder = translations[currentLang][key];
        }
    });
    
    // Update terminal initial messages
    updateTerminalInitialMessages();
}

// ===== Update Terminal Initial Messages =====
function updateTerminalInitialMessages() {
    const terminalBody = document.getElementById('terminalBody');
    if (!terminalBody) return;
    
    const lines = terminalBody.querySelectorAll('.terminal-line');
    if (lines.length >= 3) {
        if (translations[currentLang]) {
            lines[0].querySelector('.terminal-text').textContent = translations[currentLang]['terminal.welcome'] || 'Welcome to Konstryktor!';
            lines[1].querySelector('.terminal-text').textContent = translations[currentLang]['terminal.description'] || 'Project constructor for developers';
            lines[2].querySelector('.terminal-text').textContent = translations[currentLang]['terminal.select'] || 'Select a development tool to get instructions...';
        }
    }
}

// ===== Theme Toggle =====
document.addEventListener('DOMContentLoaded', () => {
    const themeToggle = document.getElementById('themeToggle');
    const body = document.body;
    
    if (!themeToggle) return;
    
    // Check saved theme or default to dark
    const savedTheme = localStorage.getItem('theme') || 'dark';
    if (savedTheme === 'dark') {
        body.classList.add('dark-theme');
        body.classList.remove('light-theme');
        const icon = themeToggle.querySelector('.theme-icon');
        if (icon) icon.textContent = '‚òÄÔ∏è';
    } else {
        body.classList.add('light-theme');
        body.classList.remove('dark-theme');
        const icon = themeToggle.querySelector('.theme-icon');
        if (icon) icon.textContent = 'üåô';
    }
    
    themeToggle.addEventListener('click', () => {
        if (body.classList.contains('light-theme')) {
            body.classList.remove('light-theme');
            body.classList.add('dark-theme');
            const icon = themeToggle.querySelector('.theme-icon');
            if (icon) icon.textContent = '‚òÄÔ∏è';
            localStorage.setItem('theme', 'dark');
        } else {
            body.classList.remove('dark-theme');
            body.classList.add('light-theme');
            const icon = themeToggle.querySelector('.theme-icon');
            if (icon) icon.textContent = 'üåô';
            localStorage.setItem('theme', 'light');
        }
    });
    
    // Setup language switcher
    setupLanguageSwitcher();
    
    // Initial translation
    translatePage();
    
    // Initialize terminal messages after translation
    initializeTerminalMessages();
});

