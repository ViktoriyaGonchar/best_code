// ===== Terminal Update Function =====
function updateTerminal(message) {
    const terminalBody = document.getElementById('terminalBody');
    if (!terminalBody) return;
    
    // Remove cursor from last line if exists
    const lastLine = terminalBody.querySelector('.terminal-line:last-child');
    if (lastLine && lastLine.querySelector('.terminal-cursor')) {
        lastLine.remove();
    }
    
    // Add new message line
    const newLine = document.createElement('div');
    newLine.className = 'terminal-line';
    newLine.innerHTML = `
        <span class="terminal-prompt">$</span>
        <span class="terminal-text">${message}</span>
    `;
    terminalBody.appendChild(newLine);
    
    // Add cursor line
    const cursorLine = document.createElement('div');
    cursorLine.className = 'terminal-line';
    cursorLine.innerHTML = `
        <span class="terminal-prompt">$</span>
        <span class="terminal-cursor">_</span>
    `;
    terminalBody.appendChild(cursorLine);
    
    // Scroll to bottom
    terminalBody.scrollTop = terminalBody.scrollHeight;
}

