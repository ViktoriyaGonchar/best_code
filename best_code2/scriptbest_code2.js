// Create tool cards
function createToolCard(tool, gridElement) {
    const card = document.createElement('div');
    card.className = 'tool-card';
    card.innerHTML = `
        <span class="tool-icon">${tool.icon}</span>
        <span class="tool-name">${tool.name}</span>
        <div class="tool-glow"></div>
    `;
    
    card.addEventListener('click', () => {
        const modal = document.getElementById('instructionsModal');
        const modalTitle = document.getElementById('modalTitle');
        const modalBody = document.getElementById('modalBody');
        
        if (modal && modalTitle && modalBody) {
            const content = generateDetailedInstructions(tool.name);
            const titleText = currentLang === 'ru' 
                ? `${tool.name} - –ü–æ—à–∞–≥–æ–≤–∞—è –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—è`
                : `${tool.name} - Step-by-step Instructions`;
            modalTitle.textContent = titleText;
            modalBody.innerHTML = content;
            modal.classList.add('active');
            const msg = currentLang === 'ru'
                ? `–ó–∞–≥—Ä—É–∂–µ–Ω—ã –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏ –¥–ª—è: ${tool.name}`
                : `Loaded instructions for: ${tool.name}`;
            updateTerminal(msg);
        }
    });
    
    gridElement.appendChild(card);
}

