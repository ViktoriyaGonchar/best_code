// Generate detailed instructions for tools
function generateDetailedInstructions(toolName) {
    const instructions = detailedToolInstructions[toolName];
    if (!instructions) {
        return generateDefaultInstructions(toolName);
    }
    
    let html = `
        <div class="instruction-section">
            <h3>${toolName}</h3>
            <p class="tool-description">${instructions.description || ''}</p>
            <ol class="instruction-list">
    `;
    
    instructions.steps.forEach((step, index) => {
        html += `
            <li>
                <strong>${step.title}</strong>
                <p>${step.content}</p>
                ${step.commands ? `<div class="command-block"><code>${step.commands}</code></div>` : ''}
            </li>
        `;
    });
    
    html += `
            </ol>
        </div>
    `;
    
    return html;
}

